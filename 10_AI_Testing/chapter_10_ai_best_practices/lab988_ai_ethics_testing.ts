// Example 2: Bias detection
interface BiasCheck {
  area: string;
  check: string;
  status: 'pass' | 'fail' | 'warning';
  recommendation?: string;
}

function checkForBias(testData: Record<string, unknown>[]): BiasCheck[] {
  const checks: BiasCheck[] = [];

  // Check for demographic diversity
  const hasNames = testData.some((d) => 'name' in d);
  if (hasNames) {
    checks.push({
      area: 'Name Diversity',
      check: 'Test data includes diverse names',
      status: 'warning',
      recommendation: 'Ensure names represent diverse backgrounds',
    });
  }

  // Check for locale diversity
  const locales = testData.filter((d) => 'locale' in d).map((d) => d.locale);
  if (new Set(locales).size < 3) {
    checks.push({
      area: 'Locale Coverage',
      check: 'Test data covers multiple locales',
      status: 'warning',
      recommendation: 'Add more locale variations to test data',
    });
  }

  return checks;
}

// Example 3: Transparency markers
function markAsAIGenerated(testCode: string): string {
  const marker = `/**
 * @ai-generated
 * @model gpt-4
 * @date ${new Date().toISOString()}
 * @reviewed false
 * 
 * This test was generated by AI and should be reviewed by a human.
 */\n\n`;

  return marker + testCode;
}

// Example 4: Human review workflow
interface ReviewStatus {
  testName: string;
  aiGenerated: boolean;
  reviewedBy?: string;
  reviewedAt?: Date;
  approved: boolean;
  comments?: string;
}

class AITestReviewTracker {
  private reviews: Map<string, ReviewStatus> = new Map();

  addAIGeneratedTest(testName: string): void {
    this.reviews.set(testName, {
      testName,
      aiGenerated: true,
      approved: false,
    });
  }

  markReviewed(testName: string, reviewer: string, approved: boolean, comments?: string): void {
    const review = this.reviews.get(testName);
    if (review) {
      review.reviewedBy = reviewer;
      review.reviewedAt = new Date();
      review.approved = approved;
      review.comments = comments;
    }
  }

  getUnreviewedTests(): string[] {
    return Array.from(this.reviews.entries())
      .filter(([, r]) => r.aiGenerated && !r.reviewedBy)
      .map(([name]) => name);
  }
}

